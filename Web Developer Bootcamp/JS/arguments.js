function sayHello(name){
  console.log("Hello " + name + "!");
}

function greet(person1, person2, person3){
  console.log("Hello " + person1 + "!");
  console.log("Hello " + person2 + "!");
  console.log("Hello " + person3 + "!");
}

sayHello("Daniel");
sayHello("Ana");
sayHello("Emma");
sayHello("Larissa");

greet("Harry", "Ron", "Hermione");
